def get_movie_by_id(movie_id):
    """Get movie by ID."""
    return next((movie for movie in REAL_MOVIES_DATABASE if movie["id"] == movie_id), None)


def get_movies_by_genre(genre):
    """Get movies containing a specific genre."""
    return [movie for movie in REAL_MOVIES_DATABASE if genre in movie["genres"]]


def get_top_movies(limit=10):
    """Get top movies by rating."""
    return sorted(REAL_MOVIES_DATABASE, key=lambda x: x["rating"], reverse=True)[:limit]


def search_movies(query):
    """Search movies by title."""
    query = query.lower()
    return [movie for movie in REAL_MOVIES_DATABASE if query in movie["title"].lower()]


def get_movies_by_year_range(start_year, end_year):
    """Get movies within a year range."""
    return [movie for movie in REAL_MOVIES_DATABASE 
            if start_year <= movie["year"] <= end_year]


